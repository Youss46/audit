interface EditDialogProps {
  setting: VatSetting | null
  open: boolean
  onClose: () => void
  onSaved: (updated: VatSetting) => void
}

function EditDialog({ setting, open, onClose, onSaved }: EditDialogProps) {
  const [rate, setRate] = useState("")
  const [salesAcc, setSalesAcc] = useState("")
  const [purchaseAcc, setPurchaseAcc] = useState("")
  const [active, setActive] = useState(true)
  const { toast } = useToast()

  // Populate form when dialog opens with a setting
  const handleOpenChange = (isOpen: boolean) => {
    if (isOpen && setting) {
      setRate(setting.ratePercentage.toString())
      setSalesAcc(setting.salesAccount ?? "")
      setPurchaseAcc(setting.purchaseAccount ?? "")
      setActive(setting.isActive)
    }
    if (!isOpen) onClose()
  }

  const mutation = useMutation({
    mutationFn: async () => {
      if (!setting) throw new Error("Aucun paramètre sélectionné.")

      const rateNum = parseFloat(rate)
      if (isNaN(rateNum) || rateNum < 0 || rateNum > 100) {
        throw new Error("Le taux doit être compris entre 0 et 100 %.")
      }
      if (salesAcc && !/^\d{4,8}$/.test(salesAcc)) {
        throw new Error("Le compte TVA collectée doit être un code SYSCOHADA de 4 à 8 chiffres.")
      }
      if (purchaseAcc && !/^\d{4,8}$/.test(purchaseAcc)) {
        throw new Error("Le compte TVA déductible doit être un code SYSCOHADA de 4 à 8 chiffres.")
      }

      return apiFetch<VatSetting>(`/cabinet/vat-settings/${setting.id}`, {
        method: "PUT",
        body: JSON.stringify({
          ratePercentage: rateNum,
          salesAccount: salesAcc || null,
          purchaseAccount: purchaseAcc || null,
          isActive: active,
        }),
      })
    },
    onSuccess: (updated) => {
      toast({
        title: "Paramètre TVA mis à jour",
        description: `Le code ${updated.code} a été enregistré avec succès.`,
      })
      onSaved(updated)
      onClose()
    },
    onError: (err: Error) => {
      toast({
        title: "Erreur de mise à jour",
        description: err.message,
        variant: "destructive",
      })
    },
  })

  if (!setting) return null

  return (
    <Dialog open={open} onOpenChange={handleOpenChange}>
      <DialogContent className="sm:max-w-md">
        <DialogHeader>
          <DialogTitle className="flex items-center gap-2">
            <Pencil className="h-4 w-4 text-primary" />
            Modifier — {setting.label}
          </DialogTitle>
          <DialogDescription>
            Ajustez le taux ou les comptes SYSCOHADA associés à ce code TVA. Toute
            modification prend effet immédiatement sur les nouvelles factures et
            écritures comptables.
          </DialogDescription>
        </DialogHeader>

        <div className="space-y-5 py-2">
          {/* Rate */}
          <div className="space-y-2">
            <Label htmlFor="vat-rate" className="text-sm font-medium">
              Taux TVA (%)
            </Label>
            <div className="relative">
              <Input
                id="vat-rate"
                type="number"
                min="0"
                max="100"
                step="0.01"
                value={rate}
                onChange={(e) => setRate(e.target.value)}
                className="pr-8"
                placeholder="18"
              />
              <span className="pointer-events-none absolute right-3 top-1/2 -translate-y-1/2 text-sm text-muted-foreground">
                %
              </span>
            </div>
            <p className="text-xs text-muted-foreground">
              Valeur actuelle : <strong>{setting.ratePercentage} %</strong>
            </p>
          </div>

          {/* Sales account */}
          <div className="space-y-2">
            <Label htmlFor="sales-account" className="text-sm font-medium">
              Compte TVA collectée (ventes)
            </Label>
            <Input
              id="sales-account"
              value={salesAcc}
              onChange={(e) => setSalesAcc(e.target.value)}
              placeholder={setting.salesAccount ?? "ex. 443100"}
              maxLength={8}
            />
            <p className="text-xs text-muted-foreground">
              Compte SYSCOHADA crédité lors de la validation d'une facture de vente. Exemple : <code className="rounded bg-muted px-1">443100</code>
            </p>
          </div>

          {/* Purchase account */}
          <div className="space-y-2">
            <Label htmlFor="purchase-account" className="text-sm font-medium">
              Compte TVA déductible (achats)
            </Label>
            <Input
              id="purchase-account"
              value={purchaseAcc}
              onChange={(e) => setPurchaseAcc(e.target.value)}
              placeholder={setting.purchaseAccount ?? "ex. 445200"}
              maxLength={8}
            />
            <p className="text-xs text-muted-foreground">
              Compte SYSCOHADA débité pour la TVA récupérable sur biens et services. Exemple : <code className="rounded bg-muted px-1">445200</code>
            </p>
          </div>

          {/* Active toggle */}
          <div className="flex items-center justify-between rounded-lg border p-3.5">
            <div className="space-y-0.5">
              <Label className="text-sm font-medium">Code actif</Label>
              <p className="text-xs text-muted-foreground">
                Un code inactif n'apparaît plus dans les menus de saisie.
              </p>
            </div>
            <Switch checked={active} onCheckedChange={setActive} />
          </div>

          {/* Warning */}
          <Alert className="border-amber-200 bg-amber-50">
            <ShieldAlert className="h-4 w-4 text-amber-600" />
            <AlertDescription className="text-xs text-amber-800">
              Cette modification affecte toutes les nouvelles factures et déclarations TVA
              générées par votre cabinet. Les écritures déjà comptabilisées ne sont
              pas rétroactivement modifiées.
            </AlertDescription>
          </Alert>
        </div>

        <DialogFooter>
          <Button variant="outline" onClick={onClose} disabled={mutation.isPending}>
            Annuler
          </Button>
          <Button onClick={() => mutation.mutate()} disabled={mutation.isPending}>
            {mutation.isPending && <Loader2 className="mr-2 h-4 w-4 animate-spin" />}
            Enregistrer
          </Button>
        </DialogFooter>
      </DialogContent>
    </Dialog>
  )
}

