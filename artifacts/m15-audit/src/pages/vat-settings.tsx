function AccountCell({
  account,
  tooltip,
  emptyLabel,
}: {
  account: string | null
  tooltip: string
  emptyLabel: string
}) {
  if (!account) {
    return (
      <span className="text-xs italic text-muted-foreground">{emptyLabel}</span>
    )
  }

  return (
    <TooltipProvider>
      <Tooltip>
        <TooltipTrigger asChild>
          <span className="inline-flex items-center gap-1.5 cursor-default">
            <code className="rounded bg-slate-100 px-1.5 py-0.5 text-xs font-mono font-medium text-slate-700">
              {account}
            </code>
          </span>
        </TooltipTrigger>
        <TooltipContent side="top" className="max-w-xs text-xs">
          {tooltip}
        </TooltipContent>
      </Tooltip>
    </TooltipProvider>
  )
}
